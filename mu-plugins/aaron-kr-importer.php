add_action( 'admin_menu', function () {
    add_management_page(
        'Import from aaron.kr JSON',
        '📥 Import from JSON',
        'manage_options',
        'aaron-kr-importer',
        'aaron_kr_importer_page'
    );
} );

function aaron_kr_importer_page(): void {
    if ( ! current_user_can( 'manage_options' ) ) wp_die( 'Not allowed.' );

    $result  = null;
    $preview = null;

    // ── Run import ─────────────────────────────────────────────────────────────
    if (
        isset( $_POST['aaron_kr_do_import'] ) &&
        wp_verify_nonce( $_POST['_wpnonce'], 'aaron_kr_import' ) &&
        ! empty( $_FILES['import_file']['tmp_name'] )
    ) {
        $json = file_get_contents( $_FILES['import_file']['tmp_name'] );
        $data = json_decode( $json, true );

        if ( ! $data || ! isset( $data['posts'] ) ) {
            $result = [ 'error' => 'Invalid JSON file — could not parse.' ];
        } else {
            $dry_run = ! empty( $_POST['dry_run'] );
            $result  = aaron_kr_run_import( $data, $dry_run );
        }
    }

    // ── Preview uploaded file without importing ────────────────────────────────
    if (
        isset( $_POST['aaron_kr_preview'] ) &&
        wp_verify_nonce( $_POST['_wpnonce'], 'aaron_kr_import' ) &&
        ! empty( $_FILES['import_file']['tmp_name'] )
    ) {
        $json    = file_get_contents( $_FILES['import_file']['tmp_name'] );
        $data    = json_decode( $json, true );
        $preview = $data ?? [ 'error' => 'Invalid JSON' ];
    }

    ?>
    <div class="wrap">
        <h1>📥 Import Portfolio from JSON</h1>
        <p>Upload the JSON file generated by the exporter on aaronsnowberger.com.
           Posts are identified by slug — existing posts with matching slugs are
           <strong>skipped</strong> (no overwrites). Run in dry-run mode first to
           preview what will happen.</p>

        <?php if ( isset( $result['error'] ) ) : ?>
            <div class="notice notice-error"><p><?php echo esc_html( $result['error'] ); ?></p></div>
        <?php endif; ?>

        <?php if ( $result && ! isset( $result['error'] ) ) : ?>
            <div class="notice notice-success">
                <p><strong><?php echo $result['dry_run'] ? 'Dry run complete.' : 'Import complete.'; ?></strong>
                   <?php echo (int) $result['created']; ?> created,
                   <?php echo (int) $result['skipped']; ?> skipped (already exist),
                   <?php echo (int) $result['errors']; ?> errors.</p>
            </div>
            <h2>Detail log</h2>
            <table class="widefat" style="max-width:900px">
                <thead><tr>
                    <th>Post</th><th>Status</th><th>Image</th><th>Tags</th><th>Type terms</th>
                </tr></thead>
                <tbody>
                <?php foreach ( $result['log'] as $row ) : ?>
                    <tr>
                        <td>
                            <?php if ( $row['post_id'] ) : ?>
                                <a href="<?php echo esc_url( get_edit_post_link( $row['post_id'] ) ); ?>">
                                    <?php echo esc_html( $row['title'] ); ?>
                                </a>
                            <?php else : ?>
                                <?php echo esc_html( $row['title'] ); ?>
                            <?php endif; ?>
                            <br><small><code><?php echo esc_html( $row['slug'] ); ?></code></small>
                        </td>
                        <td><?php echo $row['dry_run'] ? '🔍 (dry run)' : esc_html( $row['action'] ); ?></td>
                        <td><?php echo esc_html( $row['image_status'] ); ?></td>
                        <td><small><?php echo esc_html( implode( ', ', $row['tags'] ?? [] ) ); ?></small></td>
                        <td><small><?php echo esc_html( implode( ', ', $row['type_terms'] ?? [] ) ); ?></small></td>
                    </tr>
                <?php endforeach; ?>
                </tbody>
            </table>

            <?php if ( $result['dry_run'] ) : ?>
                <p style="margin-top:1rem">
                    ↑ This was a dry run. Nothing was written to the database.
                    Re-upload the file and uncheck "Dry run" to perform the real import.
                </p>
            <?php else : ?>
                <p style="margin-top:1.5rem;padding:1rem;background:#fff3cd;border-left:4px solid #ffc107">
                    <strong>🗑 Delete this file</strong> now:
                    <code>wp-content/mu-plugins/aaron-kr-importer.php</code>
                </p>
            <?php endif; ?>
        <?php endif; ?>

        <?php if ( $preview && ! isset( $preview['error'] ) ) : ?>
            <h2>File preview</h2>
            <p>Source: <code><?php echo esc_html( $preview['source_site'] ?? '?' ); ?></code>
               | Type: <code><?php echo esc_html( $preview['source_type'] ?? '?' ); ?></code>
               → <code><?php echo esc_html( $preview['target_type'] ?? '?' ); ?></code>
               | Posts: <strong><?php echo (int) ( $preview['post_count'] ?? 0 ); ?></strong>
               | Exported: <?php echo esc_html( $preview['export_date'] ?? '?' ); ?>
            </p>
