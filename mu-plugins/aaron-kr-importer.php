/**
 * Plugin Name: Aaron KR — Portfolio Importer (aaron.kr)
 * Description: Imports the JSON produced by aaron-kr-exporter.php.
 *              Creates posts with correct custom post types, assigns taxonomy
 *              terms, uploads featured images to the correct uploads directory
 *              (https://files.aaron.kr/media/), and skips duplicates by slug.
 *              INSTALL ON: aaron.kr / lab.aaron.kr (the TARGET site)
 *              DELETE AFTER: import is complete
 * Version: 1.0.0
 */

